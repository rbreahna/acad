int	ft_bouton(int i, int j, int k)
{
	int n;

	n = i;
	if ((i >= j && i <= k) || (i <= j && i >= k))
		n = i;
	if ((j >= i && j <= k) || (j <= i && j >= k))
		n = j;
	if ((k >= i && k <= j) || (k <= i && k >= j))
		n = k;
	return (n);
}
