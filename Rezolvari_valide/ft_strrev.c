char	*ft_strrev(char *str)
{
	int		m;
	int		n;
	char	l;

	n = 0;
	m = 0;
	while (str[m] != '\0')
	{
		m++;
	}
	m--;
	while (m > n)
	{
		l = str[m];
		str[m] = str[n];
		str[n] = l;
		m--;
		n++;
	}
	return (str);
}
